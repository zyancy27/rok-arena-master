/**
 * Emergency Blueprints
 *
 * A catalog of 120+ base danger situations spanning intensity, scope, scale,
 * environment, and threat type. Each blueprint is a *seed* — the composer
 * layers modular twists on top (civilian density, time pressure mechanic,
 * dynamic factors, mood) so two generations from the same blueprint still
 * feel distinct.
 *
 * Shared between the in-engine ScenarioBrain (PvE/EvE) and the
 * `generate-emergency-location` edge function (battle scene seeding) so all
 * emergency content draws from the same diverse pool.
 *
 * Pure data — no runtime deps so it loads in Deno and the browser.
 */

