// ── Selection Helpers ────────────────────────────────────────────

export function getBlueprintsByRarity(rarity: RarityTier): EmergencyBlueprint[] {
  return EMERGENCY_BLUEPRINTS.filter((b) => b.rarity === rarity);
}

export function getBlueprintsForTier(tier: number): EmergencyBlueprint[] {
  const t = Math.max(1, Math.min(5, Math.round(tier))) as SurvivabilityFloor;
  return EMERGENCY_BLUEPRINTS.filter((b) => t >= b.minTier && t <= b.maxTier);
}

export function getBlueprintById(id: string): EmergencyBlueprint | undefined {
  return EMERGENCY_BLUEPRINTS.find((b) => b.id === id);
}

